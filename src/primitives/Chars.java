package primitives;

public class Chars {
    public static void main(String[] args) {
        /*
        Primitive char is used to store a single character
        a-z
        A-A
        0-9
        specials
        */

        char sample = ' ';
        char dollarSign = '$';
        char percentageSign = '%';
        char upperCaseA = 'A';
        char favChar = '!';

        System.out.println(upperCaseA);
        System.out.println();
    }
}
